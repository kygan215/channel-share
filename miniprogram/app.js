// 注册小程序应用实例
App({
  // 小程序启动时执行
  onLaunch: function () {
    // 仅用于导出功能的云函数调用
    if (!wx.cloud) {
      // 基础库版本过低，提示用户
      console.error('请使用 2.2.3 或以上的基础库以使用云能力');
    } else {
      // 初始化云开发环境（环境ID需与云控制台一致）
      wx.cloud.init({
        env: 'cloud1-d7gvio3817ca58eb3',   // 云环境ID
        traceUser: true, // 追踪用户
      });
    }
  },
});
